#Loops com Listas
names = ['Maicon', 'Gustavo', 'Bia', 'Gabriel']

for n in names:
    print(n)

#loops com tuplas
names = ('Maicon', 'Gustavo', 'Bia', 'Gabriel')

for n in names:
    print(n)

#loops com variavel
names = 'Maicon'

for lyrics in names:
    print(lyrics)

#testando loops com tuplas de frutas
frutas = ('Maça', 'Uva', 'Morango', 'Banana')

for f in frutas:
    print(f)